export const calculadora = {
    somar: (n1, n2) => {
        const result = n1 + n2;
        return result
    },
    subtrair: (n1, n2) => {
        const result = n1 - n2;
        return result
    },
    multiplicar: (n1, n2) => {
        const result = n1 * n2;
        return result
    },
    dividir: (n1, n2) => {
        const result = n1 / n2;
        return result
    }
}

export default calculadora;